<section class="rooms-section spad">
    <div class="container">
        <div class="faq-container">
            <div class="faq-question shadow-sm " onclick="toggleFAQ(this)">
                What does Real LIFE provide for its scholars?
            </div>
            <div class="faq-answer">
                Real LIFE helps scholars finish their education by covering their tuition and miscellaneous fees,
                school-related expenses, and weekly allowances. Real LIFE also prepares its scholars for life by
                providing character formation and leadership development programs.
            </div>

            <div class="faq-question shadow-sm " onclick="toggleFAQ(this)">
                Who is qualified to apply for a Real LIFE scholarship?
            </div>
            <div class="faq-answer">
                To be eligible to receive the Real LIFE scholarship, applicants must:
                <ul>
                    <li>Be a Filipino citizen</li>
                    <li>Have a grade weighted average (GWA) of at least 88% or its equivalent in the previous
                        academic year for high school students or the previous semester for college students</li>
                    <li>Have a combined monthly household income of no more than 15,000 PHP</li>
                    <li>Not be married, have children, or be older than 25 years of age</li>
                    <li>Be enrolled at a school recognized by the Department of Education (DepEd) or Commission on
                        Higher Education (CHED)</li>
                    <li>For high school applicants, be currently studying or willing to transfer to a public school
                    </li>
                    <li>For college applicants, have tuition fees of not more than 15,000 PHP per semester</li>
                    <li>Be willing to undergo our character formation and leadership development programs</li>
                </ul>
            </div>
            <div class="faq-question shadow-sm " onclick="toggleFAQ(this)">
                I’m a college student and my tuition fees are beyond 15,000 PHP. Does this mean I can’t apply
                anymore?
            </div>
            <div class="faq-answer">
                To be eligible to receive the Real LIFE scholarship, applicants must:
                You may still apply for a Real LIFE scholarship if your school grants you an academic scholarship
                that will cover your tuition fees until graduation. If you are accepted into the Real LIFE
                scholarship program, your weekly allowance and other school-related expenses will be covered by Real
                LIFE.
            </div>
            <div class="faq-question shadow-sm " onclick="toggleFAQ(this)">
                What is the application process and how long does it take?</div>
            <div class="faq-answer">
                <li style="margin-bottom: 5px;">The first stage of the application process is a paper screening
                    where our local center teams review all the application forms and make a shortlist of those who
                    will move on to the next stage.</li>
                <li style="margin-bottom: 5px;">The second stage is a panel screening or interview where our local
                    center teams meet with applicants to know them more. Victory pastors, psychologists, social
                    workers, and Every Nation Campus missionaries are also invited to join the panel screening.
                    After this, the local center teams will make another shortlist of those who are qualified to
                    proceed to the next stage.</li>
                <li style="margin-bottom: 5px;">The third stage is a home visit for each applicant. This is
                    conducted to verify the information given by applicants in their application forms and panel
                    screenings. This is also an opportunity for our local center teams to meet the applicants'
                    families and explain the scholarship program to them. We value home visits because we want to
                    build partnerships with the families of our scholars and know how we can provide the best
                    support to their children. After the home visits, the local center teams will send the final
                    list of recommended applicants to the Real LIFE national office.</li>
                <li style="margin-bottom: 5px;">Once all local center teams have submitted their recommendations,
                    the scholarship team at the Real LIFE national office will deliberate and select the applicants
                    who will be accepted into the program.</li>
                <li style="margin-bottom: 5px;">After the final deliberation, chosen applicants will receive a
                    letter from the Real LIFE national office, confirming their acceptance into the scholarship
                    program.</li>
                <li style="margin-bottom: 5px;">The entire screening process runs from January to June each year.
                </li>
                <li style="margin-bottom: 5px;">We start accepting applications every November.</li>
                </ul>
            </div>
            <div class="faq-question shadow-sm " onclick="toggleFAQ(this)">
                Does Real LIFE accept applicants in the middle of the school year?
            </div>
            <div class="faq-answer">
                Since Real LIFE grants scholarships per school year instead of per semester, we do not accept
                applicants in the middle of the school year.
            </div>
            <div class="faq-question shadow-sm " onclick="toggleFAQ(this)">
                Can I apply for the scholarship even if I’m no longer a first-year student in high school or
                college?
            </div>
            <div class="faq-answer">
                Yes, you may. The scholarship is open to high school and college students from any year level.
            </div>
            <div class="faq-question shadow-sm " onclick="toggleFAQ(this)">
                Do I have to prepare other documents to apply for a scholarship?
            </div>
            <div class="faq-answer">
                <strong>General Forms</strong>
                <ul>
                    <li>A Real LIFE scholarship application form, completely filled out</li>
                    <li> An official and complete transcript of school records</li>
                    <li>Photocopies of income tax returns (ITR) or monthly pay slips of all earning household members,
                        or a Case Study Report from the Department of Social Welfare and Development (DSWD)</li>
                    <li>Two reference forms (available at local Real LIFE centers), filled out and signed by an
                        applicant’s class advisor, professor, church leader, or community leader</li>
                </ul>
                <br>
                <strong>Requirements for college applicants:</strong>
                <ul>
                    <li>The official grading system of the applicant’s school with grade equivalents</li>
                    <li> The course curriculum indicating the list of all required subjects to complete the course
                        of
                        study</li>
                    <li>The projected tuition fee for the incoming semester or school year, or an enrollment
                        assessment
                        form</li>
                </ul>
                <br>
                <strong>Additional requirements for incoming first-year college applicants</strong>
                <ul>
                    <li>Photocopies of reports cards from Grades 11 and 12</li>
                    <li>An admission slip or any proof that the applicant will be enrolled in the requested
                        university.
                    </li>
                </ul>
            </div>
            <div class="faq-question shadow-sm " onclick="toggleFAQ(this)">
